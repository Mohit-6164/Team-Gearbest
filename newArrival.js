let cartNewArrivalLs= JSON.parse(localStorage.getItem("newCart")) || [];

newArrival.map(function (elem, index, arr) {
    let boxNewarrival = document.createElement("div");

    let productImage = document.createElement("img");
    productImage.setAttribute("src", elem.newImage);

    let productName = document.createElement("p");
    productName.innerText = elem.newProductName;

    let productPrice = document.createElement("h3");
    productPrice.innerText = elem.newPrice;

    let cartIcon = document.createElement("img");
    cartIcon.setAttribute("src", elem.cartBtn);
    cartIcon.style.width = "25px";
    cartIcon.style.height = "25px";
    cartIcon.addEventListener("click", function(){
       if(newArrivalCart(elem.newProductID)===true){
        alert("Product Added Successfully");
        cartNewArrivalLs.push(elem);
        localStorage.setItem("newCart", JSON.stringify(cartNewArrivalLs));
       }
       else{
        alert("Product is alredy in the cart");
       }
    })


    boxNewarrival.append(productImage, productName, productPrice, cartIcon);
    document.querySelector("#newContainer").append(boxNewarrival);


})


function newArrivalCart(id){
    for(let i=0;i<cartNewArrivalLs.length;i++){
        if(cartNewArrivalLs[i].newProductID==id){
            return false;
        }
    }
    return true;
}

























